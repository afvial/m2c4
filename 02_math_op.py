# Mathematical Operators in Python

print('Addition')
print(100 + 42)
print()


print('Substraction')
print(100 - 42)
print()

print('Division')
print(100 / 42)
print(100 / 38)
print()


print('Floor Division')
print(100 // 42)
print(100 // 38)
print()

print('Multiplication')
print(100 * 38)
print()

print('Modulus')
print(100 % 42)
print()

print('Exponents')
print(11 ** 2)
print()
